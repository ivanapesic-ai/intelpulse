// Helper functions
export const getQuadrantTechnologies = (quadrant: TechnologyQuadrant): Technology[] => {
  return technologies.filter((t) => t.quadrant === quadrant);
};

export const getRingTechnologies = (ring: TechnologyRing): Technology[] => {
  return technologies.filter((t) => t.ring === ring);
};

export const getTechnologyById = (id: string): Technology | undefined => {
  return technologies.find((t) => t.id === id);
};

export const getTopTechnologies = (count: number = 5): Technology[] => {
  return [...technologies].sort((a, b) => b.compositeScore - a.compositeScore).slice(0, count);
};

export const getTrendingTechnologies = (): Technology[] => {
  return technologies.filter((t) => t.trend === "up").sort((a, b) => b.signals.investment - a.signals.investment);
};

// Stats
export const getStats = () => {
  const totalPatents = technologies.reduce((sum, t) => sum + t.patents, 0);
  const totalFunding = technologies.reduce((sum, t) => sum + t.fundingEur, 0);
  const avgTrl = technologies.reduce((sum, t) => sum + t.trl, 0) / technologies.length;
  
  return {
    totalTechnologies: technologies.length,
    totalPatents,
    totalFunding,
    avgTrl: avgTrl.toFixed(1),
    quadrantCounts: {
      Cloud: getQuadrantTechnologies("Cloud").length,
      Edge: getQuadrantTechnologies("Edge").length,
      IoT: getQuadrantTechnologies("IoT").length,
      "AI/ML": getQuadrantTechnologies("AI/ML").length,
    },
    ringCounts: {
      Adopt: getRingTechnologies("Adopt").length,
      Trial: getRingTechnologies("Trial").length,
      Assess: getRingTechnologies("Assess").length,
      Hold: getRingTechnologies("Hold").length,
    },
  };
};

export const formatFunding = (amount: number): string => {
  if (amount >= 1000000000) {
    return `€${(amount / 1000000000).toFixed(1)}B`;
  }
  if (amount >= 1000000) {
    return `€${(amount / 1000000).toFixed(0)}M`;
  }
  return `€${amount.toLocaleString()}`;
};

export const formatNumber = (num: number): string => {
  if (num >= 1000) {
    return `${(num / 1000).toFixed(1)}k`;
  }
  return num.toString();
};
